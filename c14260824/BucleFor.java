package c14260824;

public class BucleFor {
    //el bucle for ofrece la misma sintaxis, la creacion de una variable que funciona para el bucle, la condion para el termino y la operacion a realizar sobre la variable
    public static void main(String[] args) {

        for (int i = 10; i >= 0; i --) {
            System.out.println("el despegue es en " + i + " segundos");
        }
        System.out.println("despegue!");

    }
}
